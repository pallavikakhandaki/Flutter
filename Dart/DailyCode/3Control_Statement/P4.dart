//Switch Case

void main(){
  int x = 3;
  switch(x){

    case 1:
        print("Mon");
    case 2:
        print("Tue");
    case 3:
        print("Wed");
    case 4:
        print("Thurs");
    case 5:
        print("Fri");
    case 6:
        print("Sat");
    case 7:
        print("Sun");
    default:
        print("Wrong input");
  }
}

//default case should be last in switch case
