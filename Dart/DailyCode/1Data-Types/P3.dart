//Num - number

//num is use for the int and double means num is the parent of the int and float

void main(){
  int x = 10;                     
  double y = 20.5;               

  print(x);                          //10
  print(y);                          //20.5
  
  print(x.runtimeType);              //int
  print(y.runtimeType);              //float

  num a = 10;
  num b = 20.5;

  print(a);                          //10
  print(b);                          //20.5

  print(a.runtimeType);              //int
  print(b.runtimeType);              //double

}